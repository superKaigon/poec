	private void loadAsciiRandom() {
		ascii.clear();

		width = 2;
		height = 2;

		for (int i = 0; i < height; i++) {
			String sub = "";
			for (int j = 0; j < width * 27; j++) {
				int result = 0;
				Random rand = new Random();
				int max = 128;
				int min = 32;
				result = rand.nextInt(max - min + 1) + min;
				sub += (char) result;
			}
			ascii.add(sub);
		}
	}

